CREATE OR REPLACE TYPE COLEC_HIJOS2 AS VARRAY(10) OF VARCHAR2(30);
/

CREATE TABLE EMPS(
    ID NUMBER,
    NOMBRE VARCHAR2(30),
    APELLIDOS VARCHAR(30),
    HIJOS COLEC_HIJOS2
);

