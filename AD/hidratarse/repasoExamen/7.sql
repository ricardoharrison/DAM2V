CREATE OR REPLACE TYPE COLEC_TIPO_NOMBRES_DEPT AS VARRAY(7) OF VARCHAR(30);
/

CREATE TABLE DEPART(
    REGION VARCHAR(25),
    NOMBRES_DEPT COLEC_TIPO_NOMBRES_DEPT
);

